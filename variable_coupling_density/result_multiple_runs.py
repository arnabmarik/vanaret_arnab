import pandas as pd
import matplotlib.pylab as plt

fig1 = plt.figure(1)
for i in range(3):
    ax = fig1.add_subplot(3, 2, [1, 3, 5][i])
    result = pd.read_csv("result_multiple_runs(5,3).csv")
    ax.plot(range(10), result['total time[MDF]'][10 * i:10 * (i+1)], label='MDF')
    ax.plot(range(10), result['total time[IDF]'][10 * i:10 * (i+1)], label='IDF_TOL = 1e-6')
    ax.plot(range(10), result['total time[IDF_tol]'][10 * i:10 * (i+1)], label='IDF_TOL = 1e-3')
    plt.gca().legend(('MDF', 'IDF_TOL = 1e-6', 'IDF_TOL = 1e-3'), fontsize=5)
    plt.xlabel('ny', fontsize=10)
    plt.title('nx = 5, ny= 3, density =' + str([.3, .4, .5][i]), fontsize=10)
    plt.ylabel('wall time')

    ax = fig1.add_subplot(3, 2, [2, 4, 6][i])
    result = pd.read_csv("result_multiple_runs(5,3).csv")
    ax.plot(range(10), result['final_objective[MDF]'][10 * i:10 * (i+1)], label='MDF')
    ax.plot(range(10), result['final_objective[IDF]'][10 * i:10 * (i+1)], label='IDF_TOL = 1e-6')
    ax.plot(range(10), result['final_objective[IDF_tol]'][10 * i:10 * (i+1)], label='IDF_TOL = 1e-3')
    plt.gca().legend(('MDF', 'IDF_TOL = 1e-6', 'IDF_TOL = 1e-3'), fontsize=5)
    plt.xlabel('runs', fontsize=10)
    plt.title('nx = 5, ny= 3, density =' + str([.3, .4, .5][i]), fontsize=10)
    plt.ylabel('final_objective')
plt.tight_layout()

